export default function FProdName(x) {
	if (x == 1) {
		return 'Du gaz';
	} else if (x == 2) {
		return 'Des documents';
	} else if (x == 3) {
		return 'De la Nourriture';
	} else if (x == 4) {
		return 'Des Hommes';
	} else if (x == 5) {
		return 'Des cartons';
	} else if (x == 6) {
		return 'Des sacs de produits';
	} else if (x == 7) {
		return 'Des produits frais';
	} else if (x == 8) {
		return 'Des vêtements';
	} else if (x == 9) {
		return 'Des médicaments';
	} else if (x == 10) {
		return 'Des prélévements';
	} else if (x == 11) {
		return 'Des bagages';
	} else if (x == 12) {
		return 'Des ordures';
	} else if (x == 13) {
		return 'Des piéces mécaniques';
	}else if (x == 14) {
		return 'Du Matériel';
	}
}
